export function getPalette(appType, mood) {
  const category = PALETTES[appType]
  if (!category) return FALLBACK
  return category[mood] || category.default || FALLBACK
}

// ── CODE GENERATORS ──
export function generateCode(palette, approach, framework) {
  const [bg, surface, primary, accent, text] = palette.colors.map(c => c.hex)
  const files = []

  if (approach === 'css-vars') {
    if (framework === 'css' || framework === 'both') files.push({
      label: 'colors.css', filename: 'colors.css',
      code: `:root {\n  --color-bg:      ${bg};\n  --color-surface: ${surface};\n  --color-primary: ${primary};\n  --color-accent:  ${accent};\n  --color-text:    ${text};\n}\n\n/* Usage */\n.btn-primary {\n  background: var(--color-primary);\n  color: var(--color-text);\n}\nbody {\n  background: var(--color-bg);\n  color: var(--color-text);\n}`
    })
    if (framework === 'tailwind' || framework === 'both') files.push({
      label: 'tailwind.config.js', filename: 'tailwind.config.js',
      code: `module.exports = {\n  theme: {\n    extend: {\n      colors: {\n        brand: {\n          bg:      '${bg}',\n          surface: '${surface}',\n          primary: '${primary}',\n          accent:  '${accent}',\n          text:    '${text}',\n        }\n      }\n    }\n  }\n}\n\n// Usage: className="bg-brand-primary text-brand-text"`
    })
  }

  else if (approach === 'css-file') files.push({
    label: 'colors.css', filename: 'colors.css',
    code: `.bg-main     { background-color: ${bg}; }\n.bg-surface  { background-color: ${surface}; }\n.bg-primary  { background-color: ${primary}; }\n.bg-accent   { background-color: ${accent}; }\n\n.text-main   { color: ${text}; }\n.text-primary{ color: ${primary}; }\n.text-accent { color: ${accent}; }\n\n.btn-primary {\n  background-color: ${primary};\n  color: ${text};\n  border: none;\n  border-radius: 10px;\n  padding: 12px 24px;\n}\n.btn-accent {\n  background-color: ${accent};\n  color: ${bg};\n  border: none;\n  border-radius: 10px;\n  padding: 12px 24px;\n}`
  })

  else if (approach === 'inline-html') files.push({
    label: 'snippet.html', filename: 'snippet.html',
    code: `<body style="background:${bg}; color:${text}">\n\n  <div style="background:${surface}; border-radius:14px; padding:20px">\n\n    <!-- Primary button -->\n    <button style="background:${primary}; color:${text};\n      border:none; border-radius:10px; padding:12px 24px">\n      Pay Now\n    </button>\n\n    <!-- Accent button -->\n    <button style="background:${accent}; color:${bg};\n      border:none; border-radius:10px; padding:12px 24px">\n      Top Up\n    </button>\n\n  </div>\n</body>`
  })

  else if (approach === 'react-obj') files.push({
    label: 'colors.js', filename: 'colors.js',
    code: `export const colors = {\n  bg:      "${bg}",\n  surface: "${surface}",\n  primary: "${primary}",\n  accent:  "${accent}",\n  text:    "${text}"\n};\n\nexport const styles = {\n  page:       { backgroundColor: colors.bg,      color: colors.text },\n  card:       { backgroundColor: colors.surface, borderRadius: "14px", padding: "20px" },\n  btnPrimary: { backgroundColor: colors.primary, color: colors.text,\n                border: "none", borderRadius: "10px", padding: "12px 24px" },\n  btnAccent:  { backgroundColor: colors.accent,  color: colors.bg,\n                border: "none", borderRadius: "10px", padding: "12px 24px" }\n};\n\n// Usage: <button style={styles.btnPrimary}>Pay Now</button>`
  })

  else if (approach === 'classname') {
    files.push({
      label: 'colors.css', filename: 'colors.css',
      code: `.page       { background: ${bg};      color: ${text}; }\n.card       { background: ${surface}; border-radius: 14px; padding: 20px; }\n.btnPrimary { background: ${primary}; color: ${text};\n              border: none; border-radius: 10px; padding: 12px 24px; }\n.btnAccent  { background: ${accent};  color: ${bg};\n              border: none; border-radius: 10px; padding: 12px 24px; }`
    })
    files.push({
      label: 'Component.jsx', filename: 'Component.jsx',
      code: `import './colors.css';\n\nexport default function PayCard() {\n  return (\n    <div className="page">\n      <div className="card">\n        <button className="btnPrimary">Pay Now</button>\n        <button className="btnAccent">Top Up</button>\n      </div>\n    </div>\n  );\n}`
    })
  }

  else if (approach === 'tailwind-util') {
    files.push({
      label: 'tailwind.config.js', filename: 'tailwind.config.js',
      code: `module.exports = {\n  theme: {\n    extend: {\n      colors: {\n        bg:      '${bg}',\n        surface: '${surface}',\n        primary: '${primary}',\n        accent:  '${accent}',\n        brand:   '${text}',\n      }\n    }\n  }\n}`
    })
    files.push({
      label: 'Component.jsx', filename: 'Component.jsx',
      code: `export default function PayCard() {\n  return (\n    <div className="min-h-screen bg-[${bg}] text-[${text}]">\n      <div className="bg-[${surface}] rounded-2xl p-5">\n        <button className="bg-[${primary}] text-[${text}] rounded-xl px-6 py-3">\n          Pay Now\n        </button>\n        <button className="bg-[${accent}] text-[${bg}] rounded-xl px-6 py-3">\n          Top Up\n        </button>\n      </div>\n    </div>\n  );\n}`
    })
  }

  else if (approach === 'styled-comp') {
    files.push({
      label: 'theme.js', filename: 'theme.js',
      code: `export const theme = {\n  colors: {\n    bg:      "${bg}",\n    surface: "${surface}",\n    primary: "${primary}",\n    accent:  "${accent}",\n    text:    "${text}",\n  }\n};\n// Wrap app: <ThemeProvider theme={theme}>...</ThemeProvider>`
    })
    files.push({
      label: 'components.js', filename: 'components.js',
      code: `import styled from 'styled-components';\n\nexport const Page = styled.div\`\n  background: \${({ theme }) => theme.colors.bg};\n  color: \${({ theme }) => theme.colors.text};\n\`;\nexport const Card = styled.div\`\n  background: \${({ theme }) => theme.colors.surface};\n  border-radius: 14px; padding: 20px;\n\`;\nexport const PrimaryBtn = styled.button\`\n  background: \${({ theme }) => theme.colors.primary};\n  color: \${({ theme }) => theme.colors.text};\n  border: none; border-radius: 10px; padding: 12px 24px;\n\`;\nexport const AccentBtn = styled.button\`\n  background: \${({ theme }) => theme.colors.accent};\n  color: \${({ theme }) => theme.colors.bg};\n  border: none; border-radius: 10px; padding: 12px 24px;\n\`;`
    })
  }

  else if (approach === 'css-modules') {
    files.push({
      label: 'styles.module.css', filename: 'styles.module.css',
      code: `.page { background: ${bg}; color: ${text}; min-height: 100vh; }\n.card { background: ${surface}; border-radius: 14px; padding: 20px; }\n.primaryButton {\n  background: ${primary}; color: ${text};\n  border: none; border-radius: 10px; padding: 12px 24px;\n}\n.accentButton {\n  background: ${accent}; color: ${bg};\n  border: none; border-radius: 10px; padding: 12px 24px;\n}`
    })
    files.push({
      label: 'Component.jsx', filename: 'Component.jsx',
      code: `import styles from './styles.module.css';\n\nexport default function PayCard() {\n  return (\n    <div className={styles.page}>\n      <div className={styles.card}>\n        <button className={styles.primaryButton}>Pay Now</button>\n        <button className={styles.accentButton}>Top Up</button>\n      </div>\n    </div>\n  );\n}`
    })
  }

  return files.length
    ? files
    : [{ label: 'colors.css', filename: 'colors.css', code: '/* Select a styling approach */' }]
}